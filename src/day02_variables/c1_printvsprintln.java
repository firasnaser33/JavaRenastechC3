package day02_variables;

public class c1_printvsprintln {

    public static void main(String  []  args){
        // first line
        System.out.println("This is println");   //println will break current line

        //second line
        System.out.print("-- this is print");    //print will never break line
        // it will continue same line that you are using

        // second line
        System.out.print("-- continue to print"); // print wont break line

        // second line
        System.out.print("-- contine print 2");

        // second line
        System.out.println("println2");// firat it will print this in second line
        // since we used println now next line will be third


        //third line
        System.out.print("print 3"); // this wont break the line

        //third line
        System.out.println("println3");

        //fourth line
        System.out.println("println4");



    }




}
